const layerByCategory: Record<FeatureCategory, FeatureDefinition["layer"]> = {
  "Advanced Hardware Filtering": "filter",
  "Performance Benchmarking": "metric",
  "OS & Compatibility": "workflow",
  "User Accounts & Tracking": "workflow",
  "Educational UI": "guide",
  "Pricing Analytics": "analytics",
  "AI Deal Intelligence": "analytics",
  "Power-User Tools": "export",
};

export const featureBlueprint: FeatureDefinition[] = Object.entries(registry)
  .flatMap(([category, titles]) =>
    titles.map((title, index) => ({
      id: `${slug(category)}-${index + 1}`,
      category: category as FeatureCategory,
      title,
      layer: layerByCategory[category as FeatureCategory],
      status:
        index < 6
          ? "ui-ready"
          : index < 14
            ? "api-ready"
            : "foundation",
    })),
  );

export const featureCounts = Object.fromEntries(
  Object.entries(registry).map(([category, titles]) => [category, titles.length]),
) as Record<FeatureCategory, number>;

export const totalFeatureCount = featureBlueprint.length;

function slug(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}
